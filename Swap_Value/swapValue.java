class SwapValue{
    public static void main(String arg[])
    {
        int a,b;
        a=20;
        b=30;
        System.out.println("The value are : a, b = "+a+", "+ + b);
        a=a+b;
        b=a-b;
        a=a-b;
        System.out.println("the swapping value is:a,b="+a+","+ + b);
    }
}

